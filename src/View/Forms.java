    private void transactionTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_transactionTableMouseClicked
        // TODO add your handling code here:
        int index = transactionTable.getSelectedRow();
        int numberFuelPump = 0;
        double litresCounter = 0;
        for (Transactions selectedTrans : transactions) {
            if (selectedTrans.getCar().getNom().equalsIgnoreCase((String) transactionTable.getValueAt(index, 1))) {
                transaction = selectedTrans;
                numberFuelPump++;
                litresCounter += selectedTrans.getQuantity();
            }
        }
        trans_date.setText(transaction.getDate().toString());
        trans_nom.setText(transaction.getCar().getNom());
        trans_plaque.setText(transaction.getCar().getPlaque());
        trans_product.setText(transaction.getProduct());
        trans_qty.setText(String.valueOf(transaction.getQuantity()));
        trans_type.setText(transaction.getCar().getCarType());
        //        Fill report section
        amazina.setText(transaction.getCar().getNom());
        pump_times.setText(String.valueOf(numberFuelPump));
        litre_count.setText(String.valueOf(litresCounter));
    }//GEN-LAST:event_transactionTableMouseClicked

