    public void populateTransactionTable() {
        transactionModel.getDataVector().removeAllElements();
        Object[] row = new Object[4];
        boolean carFound = false;
        for (Transactions transaction : transactions) {
            row[0] = transaction.getDate();
            row[1] = transaction.getCar().getNom();
            row[2] = transaction.getQuantity();
            row[3] = transaction.getCar().getPlaque();
            transactionModel.addRow(row);
        }
    }

    public void sortByLitres(double ltrs) {
        try {
            List<Transactions> sortedTransactions = new ArrayList<>();
            for (int i = 0; i < transactions.size(); i++) {
                double litresCounter = 0;
                for (int j = 0; j < transactions.size(); j++) {
                    if (transactions.get(i).getCar().getNom().equalsIgnoreCase(transactions.get(j).getCar().getNom())) {
                        System.out.println("Name: "+ transactions.get(i).getCar().getNom() + " qty" + String.valueOf(transactions.get(i).getQuantity()));
                        litresCounter += transactions.get(j).getQuantity();
                    }
                }
                System.out.println("---> decision:"+ (litresCounter >= ltrs));
                if (litresCounter >= ltrs) {
                    sortedTransactions.add(transactions.get(i));
                }
            }
            transactions = sortedTransactions;
            populateTransactionTable();
            transactions = transDao.findAll();
        } catch (Exception e) {
            e.printStackTrace();
        }

    }

    public void searchCarBy(String searchBy, String searchName) {
        model.getDataVector().removeAllElements();
        Object[] row = new Object[4];
        boolean carFound = false;
        for (Car car : cars) {
            String searchWord = searchBy == "name" ? car.getNom() : searchBy == "plaque" ? car.getPlaque() : car.getTelephone();
            System.out.println("Search word: " + searchWord + " ..search name: " + searchName);
            if (searchWord != null && searchName != null && searchWord.trim().toLowerCase().contains(searchName.trim())) {
                carFound = true;
                row[0] = car.getNom();
                row[1] = car.getTelephone();
                row[2] = car.getPlaque();
                row[3] = car.getCarType();
            }
            model.addRow(row);
        }
        if (carFound) {
            ErrorMessage.setText("Imodoka Irahari");
        } else {
            ErrorMessage.setText("Imodoka Ntabwo irikuboneka");
            model.getDataVector().removeAllElements();
        }
    }

